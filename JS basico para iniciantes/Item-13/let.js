let nome = 'João';
console.log(nome, 'nasceu em 1984.');
console.log('Em 2000', nome, 'conheceu Maria.');
console.log(nome, 'casou se com Maria em 2012.');
console.log('Maria teve um filho com', nome, 'em 2015.');
console.log('O filho de', nome, 'se chama Eduardo.');

let algo; //Declarando a váriavel
algo = 'alguma coisa'; //Inicializando a váriavel
console.log(algo);
algo ='outra coisa'; //Apartir desse ponto a váriavel 'algo' passou a ter outro valor
console.log(algo);
/*
  - Não é possível re-declarar uma váriavel
  - Não podemos criar váriaveis com palavras reservadas
  - Váriaveis precisam ter nomes significativos  
  - Não podemos começar o nome de uma váriavel com núnero
  - Os nomes das váriaveis não podem conter espaço ou traços, para não ficar confuso usamos o 'camelCase'
  - As váriaveis são 'Case-sensitive'. Significa que, por exemplo, a váriavel 'folha' é diferente da váriavel 'Folha'
  - NÃO UTILIZE 'var', UTILIZE 'let'
*/